package com.github.luischavez.jfrogger.game.component;

import com.artemis.Component;

/**
 *
 * @author Luis
 */
public class Positionable extends Component {

    private int x;
    private int y;

    public Positionable() {
        this.x = 0;
        this.y = 0;
    }

    public Positionable(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setPosition(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public void incrementX(int x) {
        this.x += x;
    }

    public void incrementY(int y) {
        this.y += y;
    }
}
